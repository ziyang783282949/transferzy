package com.spirit.porker.model;

public class RankInfo {
	private int ranking;
	private int mttScore;
	private int masteScore;
	private int score;

	public int getRanking() {
		return ranking;
	}

	public void setRanking(int ranking) {
		this.ranking = ranking;
	}

	public int getMttScore() {
		return mttScore;
	}

	public void setMttScore(int mttScore) {
		this.mttScore = mttScore;
	}

	public int getMasteScore() {
		return masteScore;
	}

	public void setMasteScore(int masteScore) {
		this.masteScore = masteScore;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	
	

}
